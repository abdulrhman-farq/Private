export const LETTER_MD = `## الإهداء

> إلى رويدا،
> أحلى شي بالدنيا إنك تحس إن فيه أحد يحبك على حقيقتك — مو على شكلك، ولا على اللي تقدّمينه. مجرد لأنك أنتِ.
> وأنا أحلى شي صار في حياتي إني عرفتك.

## الفصل الأول: اللي أحبه فيك

يا رويدا، كل ما أطالعك أحس إني أشوف نعمة جاتني بدون ما أستاهلها. مو إنك كاملة — ما فيه أحد كامل — بس إنك حقيقية. وأنا قبلك ما كنت أعرف إن الواحد يقدر يرتاح لشخص لهالدرجة.

لو أحد سألني "ليش هي؟" — والله ما أعرف من وين أبدأ. بس بحاول.

### ① طيبتها

طيبتك مو تمثيل. ما تكونين حلوة عشان الناس تحبك — أنتِ كذا من جوّا، على طبيعتك.

شفتك كيف تتعاملين مع الناس، حتى اللي ما يستاهلون، بقلب صافي. ما تحقدين، ما تحسبين حساب، ما تخزّنين شي بقلبك. وهالشي صراحة غيّرني. كنت أحسب لازم الواحد ياخذ حذره من الدنيا، وجيتي أنتِ وبيّنتي لي إن طيبة القلب مو ضعف.

وأنا بصراحة كنت أناني أكثر قبلك، أفكّر بنفسي أول. بس لما عشت معك وشفت كيف تعطين بدون ما تنتظرين مقابل، صرت أستحي من نفسي. مو لأنك قلتي لي شي — بس لأنك كنتِ قدوة وأنتِ ما تدرين.

### ② ضحكتها وطريقة كلامها

حتى لو ما أطالعك، أحس فيك. بس لما أطالعك وأنتِ تضحكين — الدنيا توقف.

ضحكتك مو مجرد صوت يا رويدا، هي إحساس. لما تضحكين من قلبك، الجو كله يتغيّر، وكل تعب يومي يطيح عني. وأحيان تضحكين وتغطّين وجهك بيدك — وهاللحظة بالذات أحلى لحظة عندي.

ولما تحكين عن شي يهمك، عيونك تسبق كلامك. أشوف اللي تبين تقولينه في نظرتك قبل لا تنطقين. وأنا وقتها أسكت، مو لأني ما عندي كلام، بس لأني أبي أسمعك وأطالعك في نفس الوقت.

### ③ ذكاؤها

ذكية بطريقة تخليني أوقف وأفكّر. مو ذكاء كتب ودراسة — ذكاء حياة. تفهمين الناس، تقرين الموقف، وتعرفين متى تتكلمين ومتى تسكتين.

أذكر مرة كنت محتار في قرار كبير، قلّبته من كل جهة وما طلعت بشي. جيتك وحكيت لك، وبجملة وحدة فتحتي لي باب ما كنت شايفه. وقلت في نفسي: "هذي شايفة اللي أنا ما أشوفه."

وهالشي يخليني فخور فيك، وبنفس الوقت يخليني أبي أكون بمستواك.

### ④ جمالها

ما أبي أقول "أنتِ حلوة" لأن الكلمة ما توفّيك. واللي أشوفه فيك أعمق من وصف عادي.

أحبك لما تكونين توّك صاحية، شعرك مبعثر وعيونك نص مغمّضة — في هاللحظة أشوفك أجمل من أي وقت. أحبك لما تلبسين شي بسيط وتمشين بالبيت، وأنا أوقف أطالعك وأقول بقلبي: "سبحان الله."

جمالك مو بس بالمناسبات. جمالك بالصباح، بالتعب، بالسكوت. جمالك إنك أنتِ، بدون ما تحاولين تكونين أحد ثاني.

### ⑤ دعمها لي

أنتِ ما وقفتي معي بس — أنتِ حملتيني.

بأوقات كنت فيها تحت، ما أبي أتكلم ولا أشوف أحد، كنتِ هناك. ما ضغطتي عليّ، ما حكمتي عليّ. قعدتي جنبي وسكتّي معي، وهالسكوت كان أقوى من أي كلام.

وبأوقات كنت أشك في نفسي — وأنا ما أحب أعترف بهالشي — كنتِ تقولين لي "أنت تقدر"، ومو بس تقولينها، تخلّيني أصدّقها. لأنك ما تجاملين، فلما تقولينها أصدّق.

وبسببك صرت أبي أكون أحسن. مو عشانك بس — عشاني أنا. بس أنتِ كنتِ السبب.

### ⑥ أشياء ما تتوقعينها

طريقتك وأنتِ نايمة: أطالعك وأنتِ نايمة، ووجهك يرتاح بطريقة تخليني أحس إن الدنيا كلها بخير. وأحيان تحطّين يدك تحت خدّك، وأنا أدعي لك بسرّي: "يا رب لا تاخذها مني." كل ليلة أحس بهالشي.

اهتمامك بتفاصيل ما أحد ينتبه لها: تذكرين أشياء أنا بنفسي أنساها. أقول إني أبي شي قبل أسابيع، وفجأة ألقاه قدّامي. وما تقولين "شفت؟ أنا سويته" — تسوينه بسكوت. وهذا مو شي عادي يا رويدا، معناه إنك تسمعيني حتى لما ما أحس إن أحد يسمعني.

قوّتك اللي ما تبيّنينها: أنتِ قوية، بس قوة هادئة. ما ترفعين صوتك ولا تثبتين نفسك بالعافية، بس أول ما تصعب الأمور أنتِ أول وحدة تثبت. وهذا يخليني أحس بالأمان، وبنفس الوقت يخليني أبي أكون قوي عشانك مثل ما أنتِ قوية عشاني.

## الفصل الثاني: ذكريات ما تنسى

حبنا بدا بسيط وكبر مع الأيام. وفيه أشياء ما تنوصف بالكلام، لازم تعيشها عشان تفهمها.

### اللحظة اللي عرفت فيها إنها "هي"

ما كانت لحظة مثل الأفلام، لا موسيقى ولا مطر ولا غروب.

كانت لحظة عادية. كنت أتكلم معك، ما أذكر عن إيش، وفجأة سكتّي ونظرتي لي نظرة فيها كل شي — اهتمام، أمان، وشي ما ينقال.

وفي هاللحظة قلبي قال كلمة وحدة: "هي."

ما احتجت أفكّر ولا أقارن ولا أسأل أحد. ما كان فيه سبب أرتّب فيه الموضوع — كان فيه يقين، وبس.

### الضحكة اللي ما وقفت

أذكر مرة كنّا قاعدين والموضوع تافه، شي ما يضحّك أصلًا، بس أنتِ ضحكتي، وضحكتك عدتني، وصرنا نضحك لين دمعت عيوننا ومسكنا بطوننا.

وكل ما نحاول نوقف، أحدنا يطالع الثاني ونرجع من الأول.

هاللحظة ما فيها حدث كبير، بس من أسعد لحظات حياتي، لأنها كانت صادقة. كانت أنا وأنتِ بس.

### اللحظة الصعبة

كان فيه وقت مرّيت فيه بفترة ثقيلة، كل شي غلط وكل شي يضغط، وما كنت أبي أحمّلك همّي، بس أنتِ حسّيتي.

ما سألتيني "وش فيك؟" بطريقة تضغط. جيتي قعدتي جنبي، مسكتي يدي وقلتي: "أنا هنا." كلمتين بس، وكانوا يكفون.

إنك تلقى أحد يحبك بسكوت، بدون ما يطلب منك تفسّر، هذي راحة ما بعدها راحة. وأنتِ كنتِ هالأحد.

### اللحظة اللي كنت فيها غلطان

وأبي أكون صادق — مو كل لحظاتنا كانت حلوة. فيه أوقات كنت غلطان. ما سمعتك صح، أو كنت مشغول ومتوتر وفشّيت فيك وأنتِ ما تستاهلين.

وأكثر شي يعوّرني مو إني غلطت — إنك سامحتيني قبل لا أعتذر، كأنك فاهمتني أكثر من نفسي. وهذا يخليني أحس بمسؤولية إني ما أكرر الغلط. مو خوف إنك تزعلين — بس لأنك تستاهلين أحسن.

### اللحظة العادية اللي صارت ذكرى

ما أذكر إيش كنّا نسوي بالضبط، يمكن بالسيارة أو قاعدين بالبيت. بس أذكر إني طالعتك وأنتِ مشغولة بشي ما تنتبهين لي، وقلت بقلبي: "هذي حياتي، وهذي اللي أبيها كل يوم."

ما كان فيها شي مميز، بس أنتِ كنتِ فيها، وهذا كفى.

### "الله يسعدك يا عبدالرحمن"

مرة شفتك تضحكين ضحكة حقيقية من القلب، ومو عشاني — عشان شي فرّحك، بس أنا كنت هناك أشوفك.

وقلت لنفسي: "الله يسعدك يا عبدالرحمن، الله أعطاك شي ما تستاهله، لا تضيّعه ولا تنساه ولا تاخذه عادي."

ولين اليوم أكررها بنفسي كل ما أشوفك.

## الفصل الثالث: اللحظات اللي غيّرت حياتي

بالأول كنت أحبك لأني أحتاجك. الحين أحتاجك لأني أحبك. والفرق بينهم كله بفضلك.

### قبل رويدا وبعدها

قبلك كنت أعيش وبس. أروح وأجي، أسوّي اللي عليّ وأرجع. ما كان ناقصني شي بالضبط، بس ما كان فيه شي يخلي الأيام تفرق عن بعض.

بعدك؟ كل يوم صار له طعم. أصحى وعندي سبب، وأرجع البيت لأني أبي أرجع مو لأني لازم. وصرت ألاحظ أشياء ما كنت أنتبه لها: لون السما، طعم القهوة، حتى الهدوء.

لو الدنيا كلها راحت وبقيتي أنتِ، بكمّل. بس لو بقي كل شي ورحتي أنتِ، ما عاد للدنيا طعم عندي.

### عقد القران

لحظة عقد القران ما أقدر أوصف لك بالضبط وش حسّيت.

كانت فرحة، بس مو فرحة صاخبة — فرحة هادئة عميقة، أبتسم من جوّا، وقلبي يقول "أخيرًا." وكان فيه شوية خوف إني أكون أقل مما تستاهلين، بس الشوق كان أكبر من الخوف.

وحسّيت إن حياتي أخيرًا صار لها معنى. صارت عشان الحب، عشانك.

وفكّرت بالمستقبل مو بخوف، بشوق، وقلت: "يا رب بارك لي فيها، وخلّني أكون اللي تستاهله."

### كيف غيّرتني

أنا مو نفس عبدالرحمن اللي كان قبل، وأنتِ السبب.

صرت أصبر أكثر لأنك علّمتيني الصبر بدون ما تقصدين. صرت أسمع أكثر لأنك سمعتيني لما ما كان أحد يسمع. وصرت أعترف بغلطي أسرع لأنك بيّنتي لي إن الاعتراف مو ضعف.

ما غيّرتيني بالكلام، غيّرتيني بوجودك. وعطيتيني حريتي وحبك في نفس الوقت، وهذا أصعب شي، وسويتيه بدون ما تحسّين.

## الفصل الرابع: وعود وأحلام

أبينا ننمو سوا، بس كل واحد فينا يكون كامل بنفسه. مثل عمودين يشيلون سقف واحد — كل واحد واقف بقوته، وسوا نبني بيت.

### خمس وعود من عبدالرحمن لرويدا

هذي مو وعود كلام، هذي وعود حياة:

**الأول:** ما أتوقف أحاول أفهمك. حتى لو تغيّرتي — وبتتغيّرين لأن هذي طبيعة الحياة — بتغيّر معك وأتعلّمك من جديد كل مرة، وما بكسل.

**الثاني:** حتى لو جيتيني وأنتِ مو بأحسن حالة — متعبة، متضايقة، مو طايقة — بكون هنا. ما بحاول أغيّرك بهاللحظة ولا أقول لك "لا تزعلين"، بس بكون جنبك وأخليك تحسّين اللي تبين.

**الثالث:** أضحّكك كل يوم، حتى لو بشي تافه أو نكتة ما تضحّك غيرنا. أبيك تضحكين، لأن ضحكتك هي اللي تقول لي إن كل شي تمام.

**الرابع:** ما آخذك عادي أبد. وأنا خايف من هالشي بصراحة، خايف الاعتياد ينسّيني قد إيش أنتِ نعمة، وعشان كذا أوعد نفسي قبل لا أوعدك: ما راح يصير.

**الخامس:** أعترف لما أغلط، ما أعاند ولا أبرر، لأنك تستاهلين واحد يحترمك أكثر من كبريائه.

### أحلامنا المشتركة

أحلم باليوم اللي يكون لنا بيتنا، نبنيه سوا بذوقنا وتفاصيلنا، فيه ركن لك وركن لي وركن نقعد فيه نسولف لين ننام.

أحلم إننا نسافر سوا، نقعد على البحر ونطالع الغروب وأنتِ مسنودة عليّ، ونسوّي ذكريات جديدة بأماكن ما رحناها.

أحلم بحياة مليانة سلام، مو مثالية — واقعية، فيها أيام حلوة وأيام صعبة، بس فيها أنا وأنتِ سوا.

### بعد عشر سنين

بعد عشر سنين أتخيّلنا قاعدين ببيتنا، وحوالينا حياة بنيناها بإيدينا. أطالعك وأشوف فيك كل السنين اللي عشناها، الحلوة والصعبة، وأحبك أكثر، مو أقل.

أتخيّل إني أمسك يدك ونتمشّى، ونتذكر هالأيام ونضحك على أشياء كنّا نستصعبها، ونقول لبعض: "الحمد لله إننا اخترنا بعض."

وأنا كل يوم معك أحس إني أكتشف نفسي أكثر.

### الخوف

وأبي أكون صادق معك بشي — أنا أخاف عليك.

مو لأنك ضعيفة، أنتِ أقوى مني بأشياء كثيرة، بس لأنك صرتي جزء مني ما أقدر أفسّره. ومو إني ما أقدر أعيش بدونك — بس الحياة بدونك ما تشبهني.

وما أقول هالكلام عشان أخوّفك أو أحمّلك، أقوله عشان تعرفين قد إيش مكانك عندي حقيقي.

### الاختلاف

وأحب حتى اختلافنا.

مو لأننا نتفق دايم — ما أحد يتفق دايم — بس لأننا نرجع لبعض مهما صار. وحتى بأشد لحظات الزعل، ولا مرة فكّرت إني أبي أحد غيرك، ولا مرة.

وأحب إننا نختلف، لأن معناه إنك إنسانة لها رأيها، ما تمشين ورايا بدون ما تفكّرين، وهذا بالضبط اللي أحبه فيك.

## الخاتمة

يا رويدا،

نص روحي منك.

ما أبي أختم بكلام كبير، أبي أختم بشي حقيقي:

**أنا ما وعدتك بحب كامل، لأني مو كامل. بس أوعدك بحب حقيقي. وكل يوم — حتى بالأيام اللي ما أحس فيها بشي، وحتى بأصعب أيامي، وحتى لما أكون أسوأ نسخة مني — بختارك. لأن الحب مو إحساس يجي ويروح، الحب قرار، وأنا قرّرت.**

— عبدالرحمن`
